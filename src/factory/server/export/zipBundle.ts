/**
 * Get dialect-specific G-code header.
 */
function getDialectHeader(dialect: string): string {
  switch (dialect) {
    case "KDT":
      return `; KDT CNC G-code
; ISO 6983-1 compatible
%`;
    case "BIESSE":
      return `; Biesse CNC G-code
; ISO standard with Biesse extensions
%`;
    case "HOMAG":
      return `; Homag CNC G-code
; ISO standard with Homag extensions
%`;
    default:
      return `; Generic G-code
%`;
  }
}

// ============================================================================
// Hash Utilities
// ============================================================================

/**
 * Calculate SHA-256 hash of content.
 */
export function calculateSha256(content: string | Buffer): string {
  return createHash("sha256").update(content).digest("hex");
}

/**
 * Truncate hash for display.
 */
export function truncateHash(hash: string, length = 16): string {
  if (hash.length <= length * 2 + 3) return hash;
  return `${hash.slice(0, length)}...${hash.slice(-8)}`;
}
