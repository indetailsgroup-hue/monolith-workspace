/**
 * Zip Bundle - Deterministic zip creation for exports
 * P2.2 Export UX (Gated)
 *
 * @version 0.12.0
 */

import { createHash } from "crypto";

// ============================================================================
// Types
// ============================================================================

export interface BundleFile {
  /** Path within the zip (e.g., "sheet_01.nc") */
  path: string;
  /** File content */
  content: string | Buffer;
  /** Optional comment */
  comment?: string;
}

export interface BundleOptions {
  /** Job ID for naming */
  jobId: string;
  /** Files to include */
  files: BundleFile[];
  /** Optional metadata to include */
  metadata?: Record<string, unknown>;
}

export interface BundleResult {
  /** The zip buffer (or base64 for mock) */
  data: Buffer | string;
  /** SHA-256 hash of the bundle */
  sha256: string;
  /** Size in bytes */
  sizeBytes: number;
  /** Filename */
  filename: string;
  /** File manifest */
  manifest: {
    path: string;
    sha256: string;
    sizeBytes: number;
  }[];
}

// ============================================================================
// Deterministic Zip Creation (Mock Implementation)
// ============================================================================

/**
 * Create a deterministic bundle.
 *
 * Note: This is a simplified implementation for MVP.
 * In production, use a proper zip library with deterministic settings.
 */
export function createBundle(options: BundleOptions): BundleResult {
  const { jobId, files, metadata } = options;

  // Sort files for determinism
  const sortedFiles = [...files].sort((a, b) => a.path.localeCompare(b.path));

  // Create manifest
  const manifest: BundleResult["manifest"] = [];
  const fileContents: string[] = [];

  for (const file of sortedFiles) {
    const content = typeof file.content === "string"
      ? file.content
      : file.content.toString("utf-8");

    const fileHash = createHash("sha256").update(content).digest("hex");
    const sizeBytes = Buffer.byteLength(content, "utf-8");

    manifest.push({
      path: file.path,
      sha256: fileHash,
      sizeBytes,
    });

    fileContents.push(`--- ${file.path} ---\n${content}`);
  }

  // Add export metadata
  const exportMeta = {
    jobId,
    exportedAt: new Date(0).toISOString(), // Fixed timestamp for determinism
    files: manifest,
    ...(metadata || {}),
  };

  const metaContent = JSON.stringify(exportMeta, null, 2);
  fileContents.push(`--- export.meta.json ---\n${metaContent}`);

  // Create "bundle" (simplified - in production use real zip)
  const bundleContent = fileContents.join("\n\n");
  const bundleBuffer = Buffer.from(bundleContent, "utf-8");
  const bundleSha256 = createHash("sha256").update(bundleBuffer).digest("hex");

  const filename = `${jobId}_export.zip`;

  return {
    data: bundleBuffer,
    sha256: bundleSha256,
    sizeBytes: bundleBuffer.length,
    filename,
    manifest,
  };
}

/**
 * Create a mock G-code bundle for testing.
 */
export function createMockGcodeBundle(
  jobId: string,
  dialect: string,
  sheetCount: number
): BundleResult {
  const files: BundleFile[] = [];

  // Add G-code files for each sheet
  for (let i = 1; i <= sheetCount; i++) {
    const sheetId = String(i).padStart(2, "0");
    files.push({
      path: `sheet_${sheetId}.nc`,
      content: generateMockGcode(jobId, sheetId, dialect),
    });
  }

  // Add manifest
  files.push({
    path: "manifest.json",
    content: JSON.stringify({
      version: "1.0.0",
      jobId,
      dialect,
      sheetCount,
      generatedAt: new Date(0).toISOString(),
      toolVersion: "IIMOS Export 0.12.0",
    }, null, 2),
  });

  return createBundle({
    jobId,
    files,
    metadata: {
      dialect,
      sheetCount,
      generatedBy: "IIMOS Export Service",
    },
  });
}

